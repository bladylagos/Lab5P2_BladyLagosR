    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        //verificar si tiene una persona seleccionada
        if (j_jugadores.getSelectedIndex() >= 0) {
            DefaultTreeModel modeloARBOL
                    = (DefaultTreeModel) jt_jugadores.getModel();
            DefaultMutableTreeNode raiz
                    = (DefaultMutableTreeNode) modeloARBOL.getRoot();
            //obtener la persona a guardar
            DefaultListModel modeloLISTA
                    = (DefaultListModel) j_jugadores.getModel();

            String nombre, apellido, nacionalidad, edad, numjugador, cantjugados, cantcopas, cantamarillas, cantrojas, yearduracion;

            nombre = ((Jugadores) modeloLISTA.get(
                    j_jugadores.getSelectedIndex())).
                    getNombre();

            apellido = ((Jugadores) modeloLISTA.get(
                    j_jugadores.getSelectedIndex())).
                    getApellido();

            nacionalidad = ((Jugadores) modeloLISTA.get(
                    j_jugadores.getSelectedIndex())).
                    getNacionalidad();
            //
            edad = ((Jugadores) modeloLISTA.get(
                    j_jugadores.getSelectedIndex())).
                    getEdad();

            numjugador = ((Jugadores) modeloLISTA.get(
                    j_jugadores.getSelectedIndex())).
                    getNumjugador();

            cantjugados = ((Jugadores) modeloLISTA.get(
                    j_jugadores.getSelectedIndex())).
                    getCantjugados();

            cantcopas = ((Jugadores) modeloLISTA.get(
                    j_jugadores.getSelectedIndex())).
                    getCantcopas();
            //
            cantamarillas = ((Jugadores) modeloLISTA.get(
                    j_jugadores.getSelectedIndex())).
                    getCantamarillas();

            cantrojas = ((Jugadores) modeloLISTA.get(
                    j_jugadores.getSelectedIndex())).
                    getCantrojas();

            yearduracion = ((Jugadores) modeloLISTA.get(
                    j_jugadores.getSelectedIndex())).
                    getYearduracion();

            //ver si ya existe la nacionalidad
            int centinela = -1;
            for (int i = 0; i < raiz.getChildCount(); i++) {//ChildCount()
                if (raiz.getChildAt(i).toString().
                        equals(nacionalidad)) {
                    //si ya existe le agrega la persona
                    DefaultMutableTreeNode p
                            = new DefaultMutableTreeNode(
                                    new Jugadores(nombre, apellido, nacionalidad, edad, numjugador, cantjugados, cantcopas, cantamarillas, cantrojas, yearduracion)
                            );
                    ((DefaultMutableTreeNode) raiz.getChildAt(i)).add(p);
                    centinela = 1;
                }
            }

            if (centinela == -1) {
                DefaultMutableTreeNode n
                        = new DefaultMutableTreeNode(nacionalidad);
                DefaultMutableTreeNode p
                        = new DefaultMutableTreeNode(
                                new Jugadores(nombre, apellido, nacionalidad, edad, numjugador, cantjugados, cantcopas, cantamarillas, cantrojas, yearduracion)
                        );

                n.add(p);
                raiz.add(n);
            }
            modeloARBOL.reload();
        } else {
            JOptionPane.showMessageDialog(this.getFrames()[0],
                    "No hay persona seleccionada");
        }

    }//GEN-LAST:event_jButton2MouseClicked

