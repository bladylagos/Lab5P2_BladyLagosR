    private void jButton11MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton11MouseClicked
        //verificar si tiene una persona seleccionada
        if (jl_personas4.getSelectedIndex() >= 0) {
            DefaultTreeModel modeloARBOL
                    = (DefaultTreeModel) jt_personas4.getModel();
            DefaultMutableTreeNode raiz
                    = (DefaultMutableTreeNode) modeloARBOL.getRoot();
            //obtener la persona a guardar
            DefaultListModel modeloLISTA
                    = (DefaultListModel) jl_personas4.getModel();
            String nombre, apellido, nacionalidad, edad, titulo, id, especialidad, informes, atendidos;

            nombre = ((Psicologos) modeloLISTA.get(
                    jl_personas4.getSelectedIndex())).
                    getNombres();

            apellido = ((Psicologos) modeloLISTA.get(
                    jl_personas4.getSelectedIndex())).
                    getApellidos();

            nacionalidad = ((Psicologos) modeloLISTA.get(
                    jl_personas4.getSelectedIndex())).
                    getNacionalidads();

            edad = ((Psicologos) modeloLISTA.get(
                    jl_personas4.getSelectedIndex())).
                    getEdads();

            titulo = ((Psicologos) modeloLISTA.get(
                    jl_personas4.getSelectedIndex())).
                    getTituloUs();

            id = ((Psicologos) modeloLISTA.get(
                    jl_personas4.getSelectedIndex())).
                    getIds();

            especialidad = ((Psicologos) modeloLISTA.get(
                    jl_personas4.getSelectedIndex())).
                    getEspecialidads();

            informes = ((Psicologos) modeloLISTA.get(
                    jl_personas4.getSelectedIndex())).
                    getCantinformess();
            
            atendidos = ((Psicologos) modeloLISTA.get(
                    jl_personas4.getSelectedIndex())).
                    getCantjugadoress();

            //ver si ya existe la nacionalidad
            int centinela = -1;
            for (int i = 0; i < raiz.getChildCount(); i++) {//ChildCount()
                if (raiz.getChildAt(i).toString().
                        equals(nacionalidad)) {
                    //si ya existe le agrega la persona
                    DefaultMutableTreeNode p
                            = new DefaultMutableTreeNode(
                                    new Psicologos(nombre, apellido, nacionalidad, edad, titulo, id, especialidad, informes, atendidos)
                            );
                    ((DefaultMutableTreeNode) raiz.getChildAt(i)).add(p);
                    centinela = 1;
                }
            }

            if (centinela == -1) {
                DefaultMutableTreeNode n
                        = new DefaultMutableTreeNode(nacionalidad);
                DefaultMutableTreeNode p
                        = new DefaultMutableTreeNode(
                                new Psicologos(nombre, apellido, nacionalidad, edad, titulo, id, especialidad, informes, atendidos)
                        );
                n.add(p);
                raiz.add(n);
            }
            modeloARBOL.reload();
        } else {
            JOptionPane.showMessageDialog(this.getFrames()[0],
                    "No hay persona seleccionada");
        }

    }//GEN-LAST:event_jButton11MouseClicked

